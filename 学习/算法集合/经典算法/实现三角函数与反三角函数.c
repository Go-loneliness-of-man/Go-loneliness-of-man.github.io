//实现三角函数、反三角函数

//求根，专用于为小于 1 的数求根
double qg(double x)
{
	double left = x, right = 1, mid = left + ((right - left) / 2), wc = 0.0000001;	//区间三点、误差 wc
	for (; mid != left; mid = left + ((right - left) / 2))							//循环查找
		if (mid*mid == x || ((mid + wc)*(mid + wc) > x) && mid*mid < x)
			return mid;
		else if (mid*mid > x)	right = mid;
		else if (mid*mid < x)	left = mid;
	return 0;
}

//测量需要的半角值下标，大角 x、小角 y
int bc(double x, double y)
{
	int i;	double temp = x - y, m;								//差值 temp、下标 i、临时角度 m
	for (i = 1, m = 45; 1; i++, m /= 2)							//循环测算下标
		if ((temp > m / 2) && (temp < m))	return i;			//差值介于之间，返回
	printf("未查找到下标\n");
	return 0;
}

//正弦，接收角度值
double sinp(double p)
{
	double x, y, f = 0;		int temp = (int)p % 360;			//当前角 x、x 的正弦值 y、符号 f、临时 temp
	if (temp % 180 == 0)	return 0;							//0、1 的特殊情况
	else if (temp % 90 == 0)	return 1;
	if (temp / 180)		f = 1;									//判断符号
	if (p > 90)	p = 90 - (double)(temp % 90);					//限定 p 在 0 ~ 90 范围内
	double s[] = {  0.706825, 0.382499, 0.194993, 0.097968,		//sin 半角值
					0.049043, 0.024529, 0.012265, 0.006133,
					0.003066, 0.001533,0.000767,0.000383,
					0.000192 },
		   c[] = {  0.707388, 0.923956, 0.980805, 0.995190,		//cos 半角值
					0.998797, 0.999699, 0.999925, 0.999981,
					0.999995, 0.999999,0.9999997059,
					0.9999999265,0.9999999816 },
		   w[] = {  45,22.5,11.25,5.625,2.8125,1.40625,			//半角值对应的角度
					0.703125,0.351563,0.175781,0.087891,
					0.0439453, 0.0219727, 0.0109863};
	for (x = w[0], y = s[0]; p - x > 0.01 || p - x < -0.01;)	//循环推算，直到误差小于 0.01
		if (p > x)												//p 大于当前角
		{
			int temp = bc(p, x);
			x += w[temp];
			y = y*c[temp] + qg(1 - y*y)*s[temp];				//和角公式、半角值下标通过 bc 计算
		}
		else if (p < x)
		{
			int temp = bc(x, p);
			x -= w[temp];
			y = y*c[temp] - qg(1 - y*y)*s[temp];				//差角公式、半角值下标通过 bc 计算
		}
		else break; 											//相等，计算完毕
	if (f)	return -y;											//180 ~ 360 返回负数
	return y;
}

//余弦
double cosp(double p)
{
	int temp = (int)p % 360, f = 0;								//临时 temp、符号 f
	if (temp == 180)	return -1;
	else if (temp == 0)	return 1;								//0、1 的特殊情况
	else if (temp % 90 == 0)	return 0;
	if (temp > 90 && temp < 270)	f = 1;						//判断符号
	double y = sinp(p);
	y = qg(1 - y*y);											//求出 cos 的绝对值
	if (f)	return -y;											//90 ~ 270 返回负数
	return y;
}

//正切
double tanp(double p)
{
	return sinp(p) / cosp(p);
}

//根据小数点后第一位四舍五入为整数
int sswr(double a)
{
	return (int)(a * 10 + 5) / 10;				//利用加 5 筛选，再用整型除 10 去掉尾部
}

